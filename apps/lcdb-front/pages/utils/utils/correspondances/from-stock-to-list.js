export const formatFromStockToList = (rows) => {
  return rows.map((row) => ({
    id: row.idArticle,
    name_en: row['English Name'],
    name_fr: row['Local Name'],
    price: row['Price'],
    comments: row['Comments'],
    count: row['Amount'],
    condition: row['Condition'],
    expansion: row['Exp.'],
    status: '',
    from: '',
    last_edited: '',
    is_signed: row['Signed?'],
    is_first_ed: !!row['FirstEd?'],
    is_altered: row['Altered?'],
    is_foil: '',
    is_reverse_holo: !!row['ReverseHolo?'],
    is_playset: '',
    game_id: '',
    language_id: row['Language'],
    currency_id: row['idCurrency'],
    product_id: row.idProduct,
    seller_id: '',
    created_at: new Date(),
    updated_at: new Date(),
    category_id: '',
    is_sealed: '',
  }));
};
